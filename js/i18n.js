let currentLang = localStorage.getItem('site_lang') || 'zh'; // Default to Chinese

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('site_lang', lang);
  
  // Update translation text logic for static HTML
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      // If element contains HTML tags like <br> span, use innerHTML, else textContent
      if(translations[lang][key].includes('<')) {
        el.innerHTML = translations[lang][key];
      } else {
        if(el.tagName === 'INPUT' && el.type !== 'submit') {
          el.placeholder = translations[lang][key];
        } else {
          el.textContent = translations[lang][key];
        }
      }
    }
  });

  // Re-render JS dynamic components if functions exist
  if (typeof renderProductsGrid === 'function') renderProductsGrid();
  if (typeof renderCart === 'function') renderCart();
  if (typeof renderCheckout === 'function') renderCheckout();

  // Update switcher UI if it exists manually
  const switcher = document.getElementById('langSwitcher');
  if(switcher && switcher.value !== lang) {
     switcher.value = lang;
  }
}

document.addEventListener('DOMContentLoaded', () => {
  // Setup language switcher
  const switcher = document.getElementById('langSwitcher');
  if (switcher) {
    switcher.value = currentLang;
    switcher.addEventListener('change', (e) => {
      setLanguage(e.target.value);
    });
  }
  // Initial translation
  setLanguage(currentLang);
});
